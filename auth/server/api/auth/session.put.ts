    waitingTime,
    waitingChargesPerMin,
    minDeliveryCharges,
    deliveryDiscountThreshold,
    deliveryDiscountAmount,
    isCostIncluded,
    isUserTrackIncluded,
    pointsValue,
    currency,
    role,
    pipelineId,
    address,
    openTime,
    closeTime,
